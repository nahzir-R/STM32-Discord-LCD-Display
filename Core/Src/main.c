void printMessage(void const *argument)
{
    char msg[32] = "";
    for(;;)
    {
        if(xQueueReceive(MessageQueue, msg, portMAX_DELAY) == pdPASS)
        {
            char *line2 = strchr(msg, '\n');
            HD44780_Clear();
            HD44780_SetCursor(0, 0);
            if(line2)
            {
                *line2 = '\0';
                line2++;
                HD44780_PrintStr(msg);
                HD44780_SetCursor(0, 1);
                HD44780_PrintStr(line2);
            }
            else
            {
                HD44780_PrintStr(msg);
            }
        }
        osDelay(1);
    }
}

void HttpPollTask(void const *argument)
{
    osDelay(3000); // wait for network to stabilize

    int sock;
    struct sockaddr_in server;
    char request[] = "GET / HTTP/1.1\r\nHost: 192.168.1.9\r\nConnection: close\r\n\r\n";
    char response[512];

    for(;;)
    {
        sock = socket(AF_INET, SOCK_STREAM, 0);
        if(sock >= 0)
        {
            server.sin_family = AF_INET;
            server.sin_port = htons(8000);
            server.sin_addr.s_addr = inet_addr("192.168.1.9");

            if(connect(sock, (struct sockaddr*)&server, sizeof(server)) == 0)
            {
                send(sock, request, strlen(request), 0);
                memset(response, 0, sizeof(response));
                recv(sock, response, sizeof(response)-1, 0);

                // Find end of HTTP headers
                char *body = strstr(response, "\r\n\r\n");
                if(body)
                {
                    body += 4;
                    int len = strlen(body);
                    while(len > 0 && (body[len-1] == '\r' || body[len-1] == '\n' || body[len-1] == ' '))
                    {
                    	body[len-1] = '\0';
                    	len--;
                    }
                    xQueueSend(MessageQueue, body, 0);
                }
            }
            closesocket(sock);
        }
        osDelay(5000); // poll every 5 seconds
    }
}


